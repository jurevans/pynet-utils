import struct


class ICMP:
    __slots__ = ("type", "code", "sum", "id", "seq")

    def __init__(self, buff):
        header = struct.unpack("<BBHHH", buff)
        self.type = header[0]
        self.code = header[1]
        self.sum = header[2]
        self.id = header[3]
        self.seq = header[4]
